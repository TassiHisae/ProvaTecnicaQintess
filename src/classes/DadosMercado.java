package classes;

public class DadosMercado {
	
	String id_preco;
	String n_prazo;
	String vl_preco;
	
	public String getId_preco() {
		return id_preco;
	}
	
	public void setId_preco(String id_preco) {
		this.id_preco = id_preco;
	}
	
	public String getN_prazo() {
		return n_prazo;
	}
	
	public void setN_prazo(String n_prazo) {
		this.n_prazo = n_prazo;
	}
	
	public String getVl_preco() {
		return vl_preco;
	}
	
	public void setVl_preco(String vl_preco) {
		this.vl_preco = vl_preco;
	}
	

}
